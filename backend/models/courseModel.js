import mongoose from "mongoose"

const courseSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    image: {
      type: String,
    },
  },
  {
    timestaps: true,
  }
)

const Course = mongoose.model("Course", courseSchema)

export default Course
